def get_greeting(personality_type='مرح'):
    """
    الحصول على تحية عشوائية حسب شخصية البوت
    
    Args:
        personality_type (str): نوع شخصية البوت
        
    Returns:
        str: تحية عشوائية
    """
    import random
    
    personality = PERSONALITIES.get(personality_type, PERSONALITIES['مرح'])
    return random.choice(personality['greeting'])

def get_response_style(message, personality_type='مرح', response_type='default'):
    """
    الحصول على أسلوب الرد حسب شخصية البوت
    
    Args:
        message (str): الرسالة الأصلية
        personality_type (str): نوع شخصية البوت
        response_type (str): نوع الرد (default, unknown, farewell)
        
    Returns:
        str: أسلوب الرد المناسب
    """
    import random
    
    personality = PERSONALITIES.get(personality_type, PERSONALITIES['مرح'])
    responses = personality['responses'].get(response_type, personality['responses']['default'])
    
    return random.choice(responses)

def format_response(message, personality_type='مرح'):
    """
    تنسيق الرد حسب شخصية البوت
    
    Args:
        message (str): الرسالة المراد تنسيقها
        personality_type (str): نوع شخصية البوت
        
    Returns:
        str: الرسالة بعد التنسيق
    """
    personality = PERSONALITIES.get(personality_type, PERSONALITIES['مرح'])
    
    # إضافة اللهجة المناسبة للشخصية
    if personality['tone'] == 'مبهج':
        message = message + " 😊"
    elif personality['tone'] == 'رسمي':
        if not message.endswith('.'):
            message = message + "."
    elif personality['tone'] == 'ساخر':
        message = message + " 😏"
    elif personality['tone'] == 'حكيم':
        message = message + " 🌟"
    elif personality['tone'] == 'غاضب':
        message = message + "! 😠"
    
    return message

